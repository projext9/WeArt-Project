package we.are.travelers.vo;


public class CartVo {
	
	private int cart_idx;
	private String member_idx;
	private int option_idx;
	private int cart_count;
	private String cart_delyn;
	
	public int getCart_idx() {
		return cart_idx;
	}
	public void setCart_idx(int cart_idx) {
		this.cart_idx = cart_idx;
	}
	public String getMember_idx() {
		return member_idx;
	}
	public void setMember_idx(String member_idx) {
		this.member_idx = member_idx;
	}
	public int getOption_idx() {
		return option_idx;
	}
	public void setOption_idx(int option_idx) {
		this.option_idx = option_idx;
	}
	public int getCart_count() {
		return cart_count;
	}
	public void setCart_count(int cart_count) {
		this.cart_count = cart_count;
	}
	public String getCart_delyn() {
		return cart_delyn;
	}
	public void setCart_delyn(String cart_delyn) {
		this.cart_delyn = cart_delyn;
	}
	
}
