package we.are.travelers.vo;


public class OptionVo {
	
	private int option_idx;
	private int item_idx;
	private String company_idx;
	private int option_number;
	private String option_name;
	private int option_price;
	private int option_stock;
	private int option_postPrice;
	private String option_delyn;
	
	public int getOption_idx() {
		return option_idx;
	}
	public void setOption_idx(int option_idx) {
		this.option_idx = option_idx;
	}
	public int getItem_idx() {
		return item_idx;
	}
	public void setItem_idx(int item_idx) {
		this.item_idx = item_idx;
	}
	public String getCompany_idx() {
		return company_idx;
	}
	public void setCompany_idx(String company_idx) {
		this.company_idx = company_idx;
	}
	public int getOption_number() {
		return option_number;
	}
	public void setOption_number(int option_number) {
		this.option_number = option_number;
	}
	public String getOption_name() {
		return option_name;
	}
	public void setOption_name(String option_name) {
		this.option_name = option_name;
	}
	public int getOption_price() {
		return option_price;
	}
	public void setOption_price(int option_price) {
		this.option_price = option_price;
	}
	public int getOption_stock() {
		return option_stock;
	}
	public void setOption_stock(int option_stock) {
		this.option_stock = option_stock;
	}
	public int getOption_postPrice() {
		return option_postPrice;
	}
	public void setOption_postPrice(int option_postPrice) {
		this.option_postPrice = option_postPrice;
	}
	public String getOption_delyn() {
		return option_delyn;
	}
	public void setOption_delyn(String option_delyn) {
		this.option_delyn = option_delyn;
	}
	
}
