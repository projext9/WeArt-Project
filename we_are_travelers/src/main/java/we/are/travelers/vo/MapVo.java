package we.are.travelers.vo;

public class MapVo {
	
	private int map_idx;
	private String company_name;
	private String map_code; 
	private String map_addr; 
	private String map_date;
	private String map_region;
	private String map_image;
	public String getMap_image() {
		return map_image;
	}
	public void setMap_image(String map_image) {
		this.map_image = map_image;
	}
	public int getMap_idx() {
		return map_idx;
	}
	public void setMap_idx(int map_idx) {
		this.map_idx = map_idx;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getMap_code() {
		return map_code;
	}
	public void setMap_code(String map_code) {
		this.map_code = map_code;
	}
	public String getMap_addr() {
		return map_addr;
	}
	public void setMap_addr(String map_addr) {
		this.map_addr = map_addr;
	}
	public String getMap_date() {
		return map_date;
	}
	public void setMap_date(String map_date) {
		this.map_date = map_date;
	}
	public String getMap_region() {
		return map_region;
	}
	public void setMap_region(String map_region) {
		this.map_region = map_region;
	}
	

}